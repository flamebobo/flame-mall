package com.flame.mall.admin.service.impl;

import cn.hutool.core.util.StrUtil;
import com.flame.mall.admin.service.UmsResourceService;
import com.flame.mall.mbg.mapper.UmsResourceMapper;
import com.flame.mall.mbg.model.UmsResource;
import com.flame.mall.mbg.model.UmsResourceExample;
import com.github.pagehelper.PageHelper;
import lombok.RequiredArgsConstructor;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Service;
import java.util.Date;
import java.util.List;

