package com.flame.mall.admin.controller;


import com.flame.mall.admin.dto.OssCallbackResult;
import com.flame.mall.admin.dto.OssPolicyResult;
import com.flame.mall.admin.service.impl.OssServiceImpl;
import com.flame.mall.util.CommonResult;
import io.swagger.annotations.Api;
import io.swagger.annotations.ApiOperation;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Controller;
import org.springframework.web.bind.annotation.RequestMapping;
import org.springframework.web.bind.annotation.RequestMethod;
import org.springframework.web.bind.annotation.ResponseBody;

import javax.servlet.http.HttpServletRequest;

