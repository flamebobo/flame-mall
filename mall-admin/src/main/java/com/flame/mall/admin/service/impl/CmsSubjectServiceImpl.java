package com.flame.mall.admin.service.impl;

import com.flame.mall.admin.service.CmsSubjectService;
import com.flame.mall.mbg.mapper.CmsSubjectMapper;
import com.flame.mall.mbg.model.CmsSubject;
import com.flame.mall.mbg.model.CmsSubjectExample;
